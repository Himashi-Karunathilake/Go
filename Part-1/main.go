// The first program in Go.

package main

import "fmt"

func main() {
	// Print a single line
	fmt.Println("Hello World!")

	// Print an empty line
	fmt.Println()

	// Declare variables to store a string and a number
	name := "Rachael"
	age := 22

	// Print the variables
	fmt.Println("Name of Programmer: ", name)
	fmt.Println("Age of Programmer: ", age)
}
